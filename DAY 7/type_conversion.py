# Type casting method

# 1.int()
a = int('9')
print("int conversion :-",a)

# 2.float()
b = float(9)
print("float conversion :-",b)

# 3. complex(r,i)
c = complex(2,3)
print("complex conversion :-",c)

# 4.str()
d = str(9)
print("string conversion :-",d)

# 5.list(seq)
e = list("rajesh")
print("list conversion :-",e)

# 6.tuple(seq)
f = tuple("rajesh")
print("tuple conversion :-",f)

# 7.set(seq)
g = set("rajesh")
print("list conversion :-",g)

